var nums=[1,2,3,400,5,6,7,8,9,10];
var sum=0;  
var div; 
var i;
/*FOR LOOP ADDITION OF ARRAY ELEMENTS*/
for(i=0; i<nums.length; i++)
{
    sum= sum - nums[i];
    
}
console.log(sum);

/*FOR LOOP DIVISION OF 2 ELEMENTS OF ARRAY*/
for(i=0; i<nums.length; i++)
{
    div= nums[i]/nums[i];
    
}
console.log(div);